"use client";

import * as React from "react";
import * as SelectPrimitive from "@radix-ui/react-select";
import { Check, ChevronDown, ChevronUp } from "lucide-react";

import { cn } from "@/lib/utils";
import { radixClassForward } from "@/components/ui/radixClassForward";
import {
  overlayStyles,
  contentPrimitiveScrollStyles,
  labelStyles,
  separatorStyles,
  itemStyles,
  checkboxItemStyles,
  itemIndicatorStyles,
  focusRingStyles,
  selectScrollEdgeButtonStyles,
} from "@/lib/ui-classes";

const Select = SelectPrimitive.Root;

const SelectGroup = SelectPrimitive.Group;

const SelectValue = SelectPrimitive.Value;

const SelectTrigger = React.forwardRef<
  React.ElementRef<typeof SelectPrimitive.Trigger>,
  React.ComponentPropsWithoutRef<typeof SelectPrimitive.Trigger>
>(({ className, children, ...props }, ref) => (
  <SelectPrimitive.Trigger
    ref={ref}
    className={cn(
      "flex h-9 w-full items-center justify-between whitespace-nowrap rounded-md border border-input bg-transparent px-3 py-2 text-sm shadow-sm ring-offset-background placeholder:text-muted-foreground focus:outline-none focus:ring-1 focus:ring-ring disabled:cursor-not-allowed disabled:opacity-50 [&>span]:line-clamp-1",
      className,
    )}
    {...props}
  >
    {children}
    <SelectPrimitive.Icon asChild>
      <ChevronDown className="h-4 w-4 opacity-50" />
    </SelectPrimitive.Icon>
  </SelectPrimitive.Trigger>
));
SelectTrigger.displayName = SelectPrimitive.Trigger.displayName;

function SelectScrollButton(
  Primitive: typeof SelectPrimitive.ScrollUpButton | typeof SelectPrimitive.ScrollDownButton,
  Icon: typeof ChevronUp | typeof ChevronDown,
) {
  const Comp = React.forwardRef<
    React.ElementRef<typeof Primitive>,
    React.ComponentPropsWithoutRef<typeof Primitive>
  >(({ className, ...props }, ref) => (
    <Primitive
      ref={ref}
      className={cn(selectScrollEdgeButtonStyles, className)}
      {...props}
    >
      <Icon className="h-4 w-4" />
    </Primitive>
  ));
  Comp.displayName = Primitive.displayName;
  return Comp;
}

const SelectScrollUpButton = SelectScrollButton(SelectPrimitive.ScrollUpButton, ChevronUp);
const SelectScrollDownButton = SelectScrollButton(SelectPrimitive.ScrollDownButton, ChevronDown);

const SelectContent = React.forwardRef<
  React.ElementRef<typeof SelectPrimitive.Content>,
  React.ComponentPropsWithoutRef<typeof SelectPrimitive.Content>
>(({ className, children, position = "popper", ...props }, ref) => (
  <SelectPrimitive.Portal>
    <SelectPrimitive.Content
      ref={ref}
      className={cn(
        contentPrimitiveScrollStyles,
        position === "popper" &&
          "data-[side=bottom]:translate-y-1 data-[side=left]:-translate-x-1 data-[side=right]:translate-x-1 data-[side=top]:-translate-y-1",
        className,
      )}
      position={position}
      {...props}
    >
      <SelectScrollUpButton />
      <SelectPrimitive.Viewport
        className={cn(
          "p-1",
          position === "popper" &&
            "h-[var(--radix-select-trigger-height)] w-full min-w-[var(--radix-select-trigger-width)]",
        )}
      >
        {children}
      </SelectPrimitive.Viewport>
      <SelectScrollDownButton />
    </SelectPrimitive.Content>
  </SelectPrimitive.Portal>
));
SelectContent.displayName = SelectPrimitive.Content.displayName;

const SelectLabel = radixClassForward(SelectPrimitive.Label, labelStyles);

const SelectItem = React.forwardRef<
  React.ElementRef<typeof SelectPrimitive.Item>,
  React.ComponentPropsWithoutRef<typeof SelectPrimitive.Item>
>(({ className, children, ...props }, ref) => (
  <SelectPrimitive.Item
    ref={ref}
    className={cn(
      itemStyles,
      className,
    )}
    {...props}
  >
    <span className={itemIndicatorStyles}>
      <SelectPrimitive.ItemIndicator>
        <Check className="h-4 w-4" />
      </SelectPrimitive.ItemIndicator>
    </span>
    <SelectPrimitive.ItemText>{children}</SelectPrimitive.ItemText>
  </SelectPrimitive.Item>
));
SelectItem.displayName = SelectPrimitive.Item.displayName;

const SelectSeparator = radixClassForward(SelectPrimitive.Separator, separatorStyles);

export {
  Select,
  SelectGroup,
  SelectValue,
  SelectTrigger,
  SelectContent,
  SelectLabel,
  SelectItem,
  SelectSeparator,
  SelectScrollUpButton,
  SelectScrollDownButton,
};
